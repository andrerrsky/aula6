// Adição
const soma = 1 + 1 + 10 + 100 + 10.5;
console.log(`Adição: ${soma}`);

// Subtração
const sub = 10 - 5;
console.log(`Subtração: ${sub}`);

// Multiplicação *
const mult = 2 * 2;
console.log("Multiplicação:", mult);

// Divisão /
const div = 100 / 2;
console.log("Divisão:", div);

// Potência **
const pot = 10 ** 2;
console.log("Potência:", pot);

// Módulo %
const mod = 10 % 3;
console.log("Módulo:", mod);

// Mistura de operadores
const result = 20 + 2 - 5 * 3;
console.log("Resultado:", result);
