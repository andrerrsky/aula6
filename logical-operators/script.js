// AND &&
const conditionA = true;
const conditionB = true;

if (conditionA && conditionB) {
  console.log("Entrou no AND");
} else {
  console.log("Não entrou no AND");
}

// OR ||
const conditionC = true;
const conditionD = false;

if (conditionC || conditionD) {
  console.log(`Entrou no OR`);
} else {
  console.log(`Não entrou no OR`);
}
